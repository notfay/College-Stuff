package model;

public class Mobil {

	private String merek;
	private int year;
	
	public Mobil(String merek, int year) {
		super();
		this.merek = merek;
		this.year = year;
	}

	public String getMerek() {
		return merek;
	}

	public int getYear() {
		return year;
	}

	
	
}
